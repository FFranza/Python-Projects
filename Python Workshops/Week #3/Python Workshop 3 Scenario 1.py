# Python Workshop #3: Scenario 1 (For Loops and Lists)
fruits = ["apple", "pear" ,"orange", "strawberry", "mango"] # List for fruits
vegetables = ["bok choy", "lettuce", "tomato", "ginger", "garlic"] # List for vegetables

for a in fruits: # For loop for fruits
    print(a) # Print out fruits

for b in vegetables: # For loop for vegetables
    print(b) # Print out vegetables